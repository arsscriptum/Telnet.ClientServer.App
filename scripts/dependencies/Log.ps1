function Write-DebugLog {
    param(
        [Parameter(Mandatory=$true, Position=0)]
        [object[]] $InputObject,
        [Parameter(Mandatory=$false)]
        [switch]$Time
    )
    foreach ($item in $InputObject) {
        foreach ($line in ($item | Out-String)) {
            Write-InteractiveHost "[DEBUG] " -NoNewLine
            if($Time){
                Write-InteractiveHost -n ("{0} | {1}" -f ((Get-Date).GetDateTimeFormats()[18]), $line) 
            }else{
                Write-InteractiveHost -n $line
            }
            
        }
    }
}

New-Alias -Name dlog -Value Write-DebugLog -Scope Global -ErrorAction Ignore -Force